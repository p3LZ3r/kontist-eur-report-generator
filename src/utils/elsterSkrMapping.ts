let skr04ToElsterMappingCache: Record<string, string> | null = null;

export function buildSkr04ToElsterMapping(): Record<string, string> {
  if (skr04ToElsterMappingCache) {
    return skr04ToElsterMappingCache;
  }

  const mapping: Record<string, string> = {};

  for (const [elsterField, skrCodes] of Object.entries(ELSTER_TO_SKR04_MAPPING)) {
    for (const skrCode of skrCodes) {
      mapping[skrCode] = elsterField;
    }
  }

  skr04ToElsterMappingCache = mapping;
  return mapping;
}

export function getElsterFieldForSkrCode(skrCode: string): string {
  const exactMapping = buildSkr04ToElsterMapping();
  if (exactMapping[skrCode]) {
    return exactMapping[skrCode];
  }

  return getElsterFieldByRangeFallback(skrCode);
}

