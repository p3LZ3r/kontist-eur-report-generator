export function isIncomeCode(skrCode: string): boolean {
  const codeNum = parseInt(skrCode, 10);
  return codeNum >= 4000 && codeNum < 5000;
}

export function isExpenseCode(skrCode: string): boolean {
  const codeNum = parseInt(skrCode, 10);
  return (codeNum >= 5000 && codeNum < 6000) || (codeNum >= 6000 && codeNum < 8000);
}

